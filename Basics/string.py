#  strings

name ="Jaimin Panchal"
#  slices
print(name[0:6])
print(name[1])
print(name[1:])


#  string methods
print(name.upper())
print(name.lower())
print(name.capitalize())
print(name.count('a'))
print(name.find('P'))
print(name.replace('Panchal','Kumar'))

# string trim methods or striping methods
print(name.strip())
print(name.lstrip())
print(name.rstrip())

